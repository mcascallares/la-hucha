package org.matias.lahucha.form;

public class ReportForm {

	private Integer fromMonth;
	private Integer fromYear;

	private Integer untilMonth;
	private Integer untilYear;

	public Integer getFromMonth() {
		return fromMonth;
	}

	public void setFromMonth(Integer fromMonth) {
		this.fromMonth = fromMonth;
	}

	public Integer getFromYear() {
		return fromYear;
	}

	public void setFromYear(Integer fromYear) {
		this.fromYear = fromYear;
	}

	public Integer getUntilMonth() {
		return untilMonth;
	}

	public void setUntilMonth(Integer untilMonth) {
		this.untilMonth = untilMonth;
	}

	public Integer getUntilYear() {
		return untilYear;
	}

	public void setUntilYear(Integer untilYear) {
		this.untilYear = untilYear;
	}
}
